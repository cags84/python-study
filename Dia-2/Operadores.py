# Operadores Matematicos

x = 6
i = 2
z = 7

# Suma
print(f"{x} + {i} es igual a {x+i}")
# Resta
print(f"{x} - {i} es igual a {x-i}")
# Multiplicacion
print(f"{x} * {i} es igual a {x*i}")
# Divisi√≥n
print(f"{x} / {i} es igual a {x/i}")
# Division al piso
print(f"{z} dividio al piso {i} es igual a {z//i}")
# Modulo
print(f"{z} modulo de {i} es igual a {z % i}")
# Potencia
print(f"{x} elevado a la {i} es igual a {x**i}")
print(f"{x} elevado a la {i} es igual a {x**3}")
# Raiz cuadrada
print(f"La raiz cuadrada de {x} es {x**0.5}")
